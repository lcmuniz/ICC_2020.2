# Um programa que pede o raio de um círculo, calcula e mostra sua área.

# Pede o raio do círculo
raio = input("Digite o raio do círculo: ")

# Converte a entrada do usuário de texto para número
raio = float(raio)

# Calcula a área 
area = 3.14 * raio * raio

# Mostra a área na tela
print("A área é", area)
