# Faça um programa que peça dois números e imprima a soma  

# 1 - Solicitar o primeiro número e armazenar em uma variável x
x = input("Digite o primeiro número: ")

# 2 - Solicitar o segundo número e armazenar em uma variável y
y = input("Digite o segundo número: ")

n1 = float(x)
n2 = float(y)

# 3 - Somar x e y e armazenar na variavel z 
z = n1 + n2

# 4 - Imprimir a soma z 
print("A soma dos números é", z)

