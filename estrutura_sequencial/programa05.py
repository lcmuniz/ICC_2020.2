# Um programa que converte metros para centímetros.

# Pede o valor em metros ao usuário
metros = input("Digite o numero de metros: ")

# Converte a entrada do usuário em número
metros = float(metros)

# Calcula o valor dos metros em centimetros
centimetros = metros * 100

# Mostra o valor em centímetros na tela
print(metros, "metros =", centimetros, 'centímetros')