# Faça um programa que peça as 4 notas bimestrais e mostre a média

# 1 - Solicitar a primeira nota e armazenar na variável nota1
nota1 = input("Digite a nota 1: ")
n1 = float(nota1)

# 2 - Solicitar a segunda nota e armazenar na variável nota2
nota2 = input("Digite a nota 2: ")
n2 = float(nota2)

# 3 - Solicitar a terceira nota e armazenar na variável nota3
nota3 = input("Digite a nota 3: ")
n3 = float(nota3)

# 4 - Solicitar a quarta nota e armazenar na variável nota4
nota4 = input("Digite a nota 4: ")
n4 = float(nota4)

# 5 - Calcular a média das notas e armazenar na variável media 
media = (n1 + n2 + n3 + n4) / 4

# 6 - Mostra a média 
print("A média é", media)




