# Faça um programa que converta metros para centímetros.

# 1 - Solicitar o valor em metros para o usuário e armazenar na variável metros 
metros = input("Digite o valor em metros: ")

metros = float(metros)

# 2 - Converter o valor em metros para centímetros e armazenar na variável centimetros
centimetros = metros * 100 

# 3 - Mostrar o valor convertido 
print(metros, "m =", centimetros, "cm")
